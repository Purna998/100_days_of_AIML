# Basic Operations on Arrays

import numpy as np

# Arithemetic Operations
a=np.array([1,2,3])
b=np.array([4,5,6])
print(a+b)
print(a*b)
print(a/b)

arr=np.array([4,16,25])
print(np.sqrt(arr))
print(np.sum(arr))
print(np.mean(arr))
print(np.median(arr))
print(np.min(arr))
print(np.max(arr))
print(np.std(arr))


