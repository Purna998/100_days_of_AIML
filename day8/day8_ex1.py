# Numpy
import numpy as np


#Creating Arrays
arr=np.array([1,2,3,4])
print(arr)

# 3*3 Zeros Matrix
zeroes=np.zeros((3,3))
print(zeroes)

# 3*3 Zeros Matrix
ones=np.ones((3,3))
print(ones)

# make a list 1 to 10 with difference of 3
range_array=np.arange(1,10,3)
print(range_array)

# Linspace-it returns the values with in the range with given interval
linspace_array=np.linspace(0,10,3)
print(linspace_array)

